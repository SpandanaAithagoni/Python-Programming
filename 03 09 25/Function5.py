#Use of operators
def add(a,b):
    c=a+b
    return c

def sub(a,b):
    c2=a-b
    return c2

def mul(a,b):
    c3=a*b
    return c3

def div(a,b):
    c4=a/b
    return c4

def squ(a,b):
    c5=a**b
    return c5

x=5
y=2
z1=add(x,y)
z2=sub(x,y)
z3=mul(x,y)
z4=div(x,y)
z5=squ(x,y)

print("Addition:",z1,"\nSubtraction:",z2,"\nMultiplication:",z3,"\nDivision:",z4,"\nSquare:",z5)